const assert = require('assert').strict;
const images = require('./images.json');

const vendor = objects.reduce((acc, e) => ({ ...acc, ...e }), {});

const state = {};

module.exports = () => {
  const cats = Object.keys(vendor);
  assert(cats.length === Object.keys(images).length);
  assert(cats.every(c => Object.keys(images).includes(c)));

  const nextIdx = (type, category) => {
    assert(cats.includes(category), category);
    assert(
      Object.keys(vendor[category]).includes(type) || !!images[category],
      JSON.stringify({ type, category }, null, 2),
    );
    if (!state[type]) state[type] = {};
    if (!state[type][category]) state[type][category] = 0;
    const prevIdx = state[type][category];
    state[type][category] = (prevIdx + 1) % state[type][category].length;
    return prevIdx;
  };

  const nextCat = () => {
    if (!state.categories) { state.categories = 0; }
    const prevIdx = state.categories;
    state.categories = (prevIdx + 1) % cats.length;
    return cats[prevIdx];
  };

  return {
    name: cat => (cat
      ? vendor[cat].names[nextIdx('names', cat)]
      : (() => {
        const newCat = nextCat();
        return vendor[newCat].names[nextIdx('names', newCat)];
      })()),

    offeringName: cat => vendor[cat].short[nextIdx('short', cat)],

    description: cat => vendor[cat].long[nextIdx('long', cat)],

    image: cat => images[cat][nextIdx('image', cat)],

    categoryByName: (name) => {
      const x = cats.find(cat => vendor[cat].names.includes(name));
      assert(!!x);
      return x;
    },
  };
};
